package Models;

/**
 *
 * @author User
 */
public class UserModel {

    private Integer userId;
    private String userName;
    private String userPassword;
    private String userRole;

    public UserModel() {
    }

    public UserModel(Integer userId, String userName, String userPassword, String userRole) {
        this.userId = userId;
        this.userName = userName;
        this.userPassword = userPassword;
        this.userRole = userRole;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }

    public String getUserRole() {
        return userRole;
    }

    public void setUserRole(String userRole) {
        this.userRole = userRole;
    }

    @Override
    public String toString() {
        return "UserObj { User Id : "+userId+", User name : "+userName+", User Password : "+userPassword+", User role: "+userRole+"}"; //To change body of generated methods, choose Tools | Templates.
    }

    

}
